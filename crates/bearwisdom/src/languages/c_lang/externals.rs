/// Dependency-gated framework globals for C/C++.
pub(crate) fn framework_globals(deps: &HashSet<String>) -> Vec<&'static str> {
    let mut globals = Vec::new();

    // Qt framework
    for dep in ["Qt5Core", "Qt6Core", "Qt5Widgets", "Qt6Widgets", "Qt5", "Qt6", "Qt"] {
        if deps.contains(dep) {
            globals.extend(QT_GLOBALS);
            break;
        }
    }

    globals
}

