/// Returns `true` for names that are built into the Nix expression language or
/// always provided by nixpkgs `lib` / `builtins` — i.e., they will never
/// appear as project-defined symbols.
pub(super) fn is_nix_builtin(name: &str) -> bool {
    // Attribute-path prefixes: builtins.*, lib.*, pkgs.lib.*, config.*
    if name.starts_with("builtins.")
        || name.starts_with("lib.")
        || name.starts_with("pkgs.lib.")
        || name.starts_with("pkgs.")
        || name.starts_with("config.")
    {
        return true;
    }
    matches!(
        name,
        // -----------------------------------------------------------------------
        // Nix language built-ins (bare names)
        // -----------------------------------------------------------------------
        "import"
            | "builtins"
            | "derivation"
            | "abort"
            | "throw"
            | "toString"
            | "toJSON"
            | "fromJSON"
            | "toPath"
            | "storePath"
            | "baseNameOf"
            | "dirOf"
            | "isNull"
            | "isBool"
            | "isInt"
            | "isFloat"
            | "isList"
            | "isAttrs"
            | "isFunction"
            | "isString"
            | "isPath"
            | "typeOf"
            | "map"
            | "filter"
            | "foldl'"
            | "foldl"
            | "foldr"
            | "head"
            | "tail"
            | "length"
            | "elem"
            | "elemAt"
            | "concatLists"
            | "concatMap"
            | "listToAttrs"
            | "attrNames"
            | "attrValues"
            | "hasAttr"
            | "getAttr"
            | "removeAttrs"
            | "mapAttrs"
            | "intersectAttrs"
            | "functionArgs"
            | "readFile"
            | "readDir"
            | "pathExists"
            | "path"
            | "fetchurl"
            | "fetchTarball"
            | "fetchGit"
            | "fetchFromGitHub"
            | "currentSystem"
            | "currentTime"
            | "nixPath"
            | "storeDir"
            | "nixVersion"
            | "tryEval"
            | "seq"
            | "deepSeq"
            | "trace"
            // -----------------------------------------------------------------------
            // Nixpkgs helpers / module system
            // -----------------------------------------------------------------------
            | "callPackage"
            | "callPackageWith"
            | "mkDerivation"
            | "stdenv"
            | "pkgs"
            | "lib"
            | "config"
            | "self"
            | "super"
            | "prev"
            | "final"
            // nixpkgs lib.* (bare names used after `with lib;`)
            | "mkIf"
            | "mkOption"
            | "mkDefault"
            | "mkForce"
            | "mkMerge"
            | "mkOverride"
            | "mkEnableOption"
            | "mkPackageOption"
            | "mkRemovedOptionModule"
            | "mkRenamedOptionModule"
            | "mkAliasOptionModule"
            | "recursiveUpdate"
            | "optionalString"
            | "optionals"
            | "optional"
            | "concatMapStrings"
            | "concatStringsSep"
            | "concatStrings"
            | "filterAttrs"
            | "mapAttrsToList"
            | "nameValuePair"
            | "flip"
            | "const"
            | "id"
            | "fix"
            | "extends"
            | "makeOverridable"
            | "getExe"
            | "getExe'"
            | "getBin"
            | "getDev"
            | "getLib"
            | "getOutput"
            | "makeSearchPath"
            | "makeBinPath"
            | "makeLibraryPath"
            | "makeIncludePath"
            | "escapeShellArg"
            | "escapeShellArgs"
            | "removeSuffix"
            | "removePrefix"
            | "hasPrefix"
            | "hasSuffix"
            | "splitString"
            | "toLower"
            | "toUpper"
            | "floatToString"
            | "intToString"
            | "strings"
            // types.*
            | "types"
    )
}
